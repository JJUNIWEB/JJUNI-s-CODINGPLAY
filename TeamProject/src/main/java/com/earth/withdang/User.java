package com.earth.withdang;

public class User {
	private String name;
	private String email;
	private String Id;
	
	public String getName() {
		return name;
	}
	public String getEmail() {
		return email;
	}
	public String getId() {
		return Id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public void setId(String id) {
		Id = id;
	}
	
	
	
}
